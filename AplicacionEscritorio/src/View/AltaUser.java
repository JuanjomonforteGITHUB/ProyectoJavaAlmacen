    private void jbCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCrearActionPerformed
        if (this.jtNombre.getText().isEmpty() || this.jtUsuario.getText().isEmpty() || this.jtPassword.getText().isEmpty()) {
            if (this.jtNombre.getText().isEmpty()) {
                this.jlErrorNombre.setVisible(true);
            } else {
                this.jlErrorNombre.setVisible(false);
            }
            if (this.jtUsuario.getText().isEmpty()) {
                this.jlErrorUsuario.setVisible(true);
            } else {
                this.jlErrorUsuario.setVisible(false);
            }

            if (this.jtPassword.getText().isEmpty()) {
                this.jlErrorPassword.setVisible(true);
            } else {
                this.jlErrorPassword.setVisible(false);
            }
        } else {
            this.jlErrorNombre.setVisible(false);
            this.jlErrorUsuario.setVisible(false);
            this.jlErrorPassword.setVisible(false);
            String nuevouserNombre = this.jtNombre.getText();
            String nuevouserApellidos = this.jtApellidos.getText();
            String nuevouserCorreo = this.jtCorreo.getText();
            String nuevouserUsuario = this.jtUsuario.getText();
            String nuevouserPassword = this.jtPassword.getText();

            CrearUserController crearUsuario = new CrearUserController();
            try {
                crearUsuario.crearUsuarioController(nuevouserNombre, nuevouserApellidos, nuevouserCorreo, nuevouserUsuario, nuevouserPassword);
            } catch (SQLException ex) {
                Logger.getLogger(AltaUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jbCrearActionPerformed

